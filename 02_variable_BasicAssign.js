// Variable declarations
var numberVariable = 123;
var stringVariable = "Hello";
var booleanVariable = true;

// Log variable values and types
console.log("numberVariable:", numberVariable, "type:", typeof numberVariable);
console.log("stringVariable:", stringVariable, "type:", typeof stringVariable);
console.log("booleanVariable:", booleanVariable, "type:", typeof booleanVariable);

// Difference between null and undefined
var nullVariable = null;
var undefinedVariable;

console.log("nullVariable:", nullVariable, "type:", typeof nullVariable);
console.log("undefinedVariable:", undefinedVariable, "type:", typeof undefinedVariable);
